    <script>
    const contactForm = document.getElementById('contact-form');
    const contactFormContent = document.getElementById('contact-form-content');
    const contactSuccess = document.getElementById('contact-success');
    if (contactForm) {
        contactForm.addEventListener('submit', async e => {
            e.preventDefault();
            const btn = contactForm.querySelector('button[type="submit"]');
            const originalText = btn.innerHTML;
            btn.disabled = true;
            btn.innerHTML = 'Sending...';
            const fd = new FormData(contactForm);
            try {
                const res = await fetch('/contact', {
                    method: 'POST',
                    headers: { 'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content },
                    body: fd,
                });
                const data = await res.json();
                if (data.success) {
                    contactForm.reset();
                    if (contactFormContent) {
                        contactFormContent.style.opacity = '0';
                        contactFormContent.style.transform = 'translateY(-8px)';
                        contactFormContent.style.pointerEvents = 'none';
                    }
                    if (contactSuccess) {
                        contactSuccess.style.display = 'flex';
                        requestAnimationFrame(() => {
                            contactSuccess.classList.add('show');
                        });
                    }
                }
            } catch (err) {
                //
            } finally {
                btn.disabled = false;
                btn.innerHTML = originalText;
            }
        });
    }
    </script>

